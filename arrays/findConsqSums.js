const expected2 = [
  [-2, -5, -3, -6],
  [-3, -6, -7],
  [-3, -6, -7, -0],
  [-3, -6, -7, -0, -0],
];

function findConsqSums(nums, targetSum) {
  let arr = [];
  for (let i = 0; i < nums.length; i++) {
    let sum = nums[i];
    for (let j = i + 1; j <= nums.length; j++) {
      if (sum == targetSum) {
        arr.push(nums.slice(i, j));
      }
      sum += nums[j];
    }
  }
  return arr;
}
